# schema\reviewer_result.py

from pydantic import BaseModel, Field


class Verdict(BaseModel):
    name: str = Field(
        description="Точная формулировка проверяемого критерия или паттерна - "
        "скопируй её дословно из списка, переданного в промпте. Не придумывай свои."
    )
    passed: bool = Field(
        description="Выполнено ли требование в коде ученика. Для пунктов, "
        "сформулированных как отрицание, ориентируйся на смысл самой формулировки: "
        "пункт считается выполненным, если верно то, что в нём написано."
    )
    comment: str = Field(
        description="Краткое обоснование вердикта со ссылкой на конкретное место в коде "
        "(имя функции, строка, конструкция). Если false - укажи, чего именно не хватает. "
        "На русском языке, одно-два предложения."
    )


class ReviewerResult(BaseModel):
    criteria: list[Verdict] = Field(
        description="По одному вердикту на каждый пункт из списка критериев задания в промпте, "
        "в том же количестве и порядке."
    )
    patterns: list[Verdict] = Field(
        description="По одному вердикту на каждый пункт из списка паттернов в промпте, "
        "в том же количестве и порядке."
    )
    main_problem: str = Field(
        description="Одна самая важная проблема кода, которую стоит исправить в первую очередь. "
        "Если серьёзных проблем нет - так и напиши. На русском языке."
    )
    strengths: str = Field(
        description="Что в коде сделано хорошо, кратко. На русском языке."
    )
